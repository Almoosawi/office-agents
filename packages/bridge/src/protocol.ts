/**
 * Map the provider-internal ChatChunk shape (`type` discriminant) onto the
 * wire ChatChunk shape (`kind` discriminant) so the taskpane never depends
 * on the bridge's internal type names. Internal shape lives in
 * providers/types.ts.
 */
export function chatChunkToWire(
  chunk: { type: string } & Record<string, unknown>,
): BridgeChatChunk {
  switch (chunk.type) {
    case "text":
      return { kind: "text", delta: chunk.delta as string };
    case "tool_call":
      return {
        kind: "tool_call",
        id: chunk.id as string,
        name: chunk.name as string,
        argsJson: chunk.argsJson as string,
      };
    case "tool_result":
      return {
        kind: "tool_result",
        id: chunk.id as string,
        resultJson: chunk.resultJson as string,
      };
    case "error":
      return {
        kind: "error",
        message: chunk.message as string,
        recoverable: chunk.recoverable as boolean | undefined,
      };
    case "done":
      return {
        kind: "done",
        reason: chunk.reason as BridgeChatChunk extends { kind: "done" }
          ? Extract<BridgeChatChunk, { kind: "done" }>["reason"]
          : never,
      };
    default:
      return { kind: "error", message: `unknown chunk type: ${chunk.type}` };
  }
}

export function extractToolText(result: unknown): string {
  if (typeof result === "string") return result;

  const candidate = result as ToolResultLike | undefined;
  if (candidate?.content && Array.isArray(candidate.content)) {
    return candidate.content
      .filter((part): part is ToolTextPart => part?.type === "text")
      .map((part) => part.text)
      .join("\n");
  }

  try {
    return JSON.stringify(result, null, 2);
  } catch {
    return String(result);
  }
}

export function extractToolImages(
  result: unknown,
): Array<{ data: string; mimeType: string }> {
  const candidate = result as ToolResultLike | undefined;
  if (!candidate?.content || !Array.isArray(candidate.content)) return [];

  return candidate.content
    .filter((part): part is ToolImagePart => part?.type === "image")
    .map((part) => ({ data: part.data, mimeType: part.mimeType }));
}

export function extractToolError(result: unknown): string | undefined {
  const text = extractToolText(result).trim();
  if (!text) return undefined;

  try {
    const parsed = JSON.parse(text);
    if (
      parsed &&
      typeof parsed === "object" &&
      (("error" in parsed && typeof parsed.error === "string") ||
        parsed.success === false)
    ) {
      return typeof parsed.error === "string"
        ? parsed.error
        : "Tool execution failed";
    }
  } catch {
    // Plain text result.
  }

  return undefined;
}

export function getDefaultRawExecutionTool(
  app: string,
): "eval_officejs" | "execute_office_js" | undefined {
  const normalized = app.toLowerCase();
  if (normalized === "excel") return "eval_officejs";
  if (normalized === "powerpoint" || normalized === "word") {
    return "execute_office_js";
  }
  return undefined;
}
