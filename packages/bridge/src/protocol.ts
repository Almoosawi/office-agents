// ---------- Chat dispatch (Module 3) ----------
//
// Direction: chat_request and chat_abort go taskpane → bridge.
// chat_chunk goes bridge → taskpane (one per ChatChunk yielded by the
// chosen provider's adapter). The terminal frame is always a chunk with
// `kind: "done"` — taskpanes use it to flip UI state out of "streaming".
//
// requestId is opaque, generated by the taskpane. Bridges don't enforce
// uniqueness across sessions, but they do reject concurrent reuse within
// a single session via the dispatcher's abort-table.

/** Chat message sent to the provider. Mirrors providers/types.ChatMessage. */
